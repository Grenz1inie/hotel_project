-- 存储过程 -----------------------------------------------------------------
-- 生成指定日期范围内的每日房间统计（写入vacancy_statistics表）
CREATE OR REPLACE PROCEDURE hotel_user.generate_daily_stats(
    p_start_date DATE,
    p_end_date   DATE
) IS
    v_stat_date DATE := p_start_date;

    v_total_rooms NUMBER;
    v_available_rooms NUMBER;
    v_occupied_rooms NUMBER;
    v_reserved_rooms NUMBER;
    v_maintenance_rooms NUMBER;
    v_locked_rooms NUMBER;
    v_vacancy_rate NUMBER(5,4);

BEGIN
    WHILE v_stat_date <= p_end_date LOOP

        FOR rec IN (
            SELECT h.id AS hotel_id, rt.id AS room_type_id
            FROM hotel_user.hotel h
            JOIN hotel_user.room_type rt ON rt.hotel_id = h.id
            WHERE rt.is_active = 1
        ) LOOP

            SELECT COUNT(*),
                   COUNT(CASE WHEN r.status = 1 THEN 1 END),
                   COUNT(CASE WHEN r.status = 3 THEN 1 END),
                   COUNT(CASE WHEN r.status = 2 THEN 1 END),
                   COUNT(CASE WHEN r.status = 5 THEN 1 END)
            INTO v_total_rooms, v_available_rooms,
                 v_occupied_rooms, v_reserved_rooms, v_maintenance_rooms
            FROM hotel_user.room r
            WHERE r.room_type_id = rec.room_type_id;

            v_locked_rooms := 0;

            IF v_total_rooms > 0 THEN
                v_vacancy_rate := v_available_rooms / v_total_rooms;
            ELSE
                v_vacancy_rate := 0;
            END IF;

            MERGE INTO hotel_user.vacancy_statistics vs
            USING (
                SELECT rec.hotel_id hid,
                       rec.room_type_id rtid,
                       v_stat_date sdate
                FROM dual
            ) src
            ON (vs.hotel_id = src.hid
                AND vs.room_type_id = src.rtid
                AND vs.stat_date = src.sdate
                AND vs.stat_hour IS NULL)

            WHEN MATCHED THEN
                UPDATE SET
                    total_rooms = v_total_rooms,
                    available_rooms = v_available_rooms,
                    occupied_rooms = v_occupied_rooms,
                    reserved_rooms = v_reserved_rooms,
                    maintenance_rooms = v_maintenance_rooms,
                    locked_rooms = v_locked_rooms,
                    vacancy_count = v_available_rooms,
                    vacancy_rate = v_vacancy_rate,
                    updated_at = SYSDATE

            WHEN NOT MATCHED THEN
                INSERT (
                    hotel_id,
                    room_type_id,
                    stat_date,
                    stat_hour,
                    total_rooms,
                    available_rooms,
                    occupied_rooms,
                    reserved_rooms,
                    maintenance_rooms,
                    locked_rooms,
                    vacancy_count,
                    vacancy_rate,
                    created_at,
                    updated_at
                )
            VALUES (
                    rec.hotel_id,
                    rec.room_type_id,
                    v_stat_date,
                    NULL,
                    v_total_rooms,
                    v_available_rooms,
                    v_occupied_rooms,
                    v_reserved_rooms,
                    v_maintenance_rooms,
                    v_locked_rooms,
                    v_available_rooms,
                    v_vacancy_rate,
                    SYSDATE,
                    SYSDATE
            );

        END LOOP;

        v_stat_date := v_stat_date + 1;
    END LOOP;

    COMMIT;
END;
/

