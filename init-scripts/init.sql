-- 插入房间
INSERT INTO
    room (
        id,
        hotel_id,
        room_type_id,
        room_number,
        FLOOR,
        status
    )
WITH
    room_type_ranked AS (
        SELECT
            id,
            hotel_id,
            total_count,
            ROW_NUMBER() OVER (
                PARTITION BY
                    hotel_id
                ORDER BY id
            ) AS type_rank
        FROM room_type
    ),
    numbers AS (
        SELECT LEVEL AS n
        FROM DUAL CONNECT BY LEVEL <= (
                SELECT MAX(total_count)
                FROM room_type
            )
    )
SELECT
    seq_room.NEXTVAL,
    rt.hotel_id,
    rt.id,
    CHR (64 + rt.type_rank) || LPAD(FLOOR((n -1) / 8) + 1, 2, '0') || LPAD(MOD(n -1, 8) + 1, 2, '0') AS room_number,
    FLOOR((n -1) / 8) + 1 AS FLOOR,
    1 AS status
FROM room_type_ranked rt
    JOIN numbers ON n <= rt.total_count;

